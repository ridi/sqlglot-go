var identifierTokens = map[tokens.TokenType]bool{tokens.VAR: true, tokens.IDENTIFIER: true}

// reservedTokens mirrors parser.py:613 RESERVED_TOKENS:
//
//	{*Tokenizer.SINGLE_TOKENS.values(), SELECT} - {IDENTIFIER}
//
// i.e. every operator/punctuation single-token plus SELECT. Keywords such as
// FROM/WHERE/JOIN/ON/AND/OR are NOT reserved here (they gate elsewhere), so
// `SELECT 1 AS from` is a valid alias while `SELECT a AS +` is a parse error.
var reservedTokens = buildReservedTokens()

func buildReservedTokens() map[tokens.TokenType]bool {
	m := map[tokens.TokenType]bool{}
	for _, tt := range tokens.BaseConfig().SingleTokens {
		m[tt] = true
	}
	m[tokens.SELECT] = true
	delete(m, tokens.IDENTIFIER)
	return m
}
